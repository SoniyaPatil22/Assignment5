# 1. Create a list of numbers from 1 to 10
numbers = list(range(1, 11))

# 2. Extract the first five elements from the list
first_five = numbers[:5]

# 3. Reverse the extracted elements
reversed_first_five = first_five[::-1]

# 4. Print both the extracted list and the reversed list
print("original list: ",numbers)
print("Extracted First five elements:", first_five)
print("Reversed extracted elements:", reversed_first_five)
